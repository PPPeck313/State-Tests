import 'package:flutter/widgets.dart';

class RetainedTab extends StatefulWidget {

  //============================================================================
  // Fields
  //============================================================================

  final Widget tab;

  //============================================================================
  // Constructors
  //============================================================================

  RetainedTab(this.tab);

  //============================================================================
  // Lifecycle Methods
  //============================================================================

  @override
  _RetainedTabState createState() => _RetainedTabState(tab);
}



class _RetainedTabState extends State<RetainedTab> with AutomaticKeepAliveClientMixin<RetainedTab> {

  //============================================================================
  // Fields
  //============================================================================

  final Widget tab;

  //============================================================================
  // Constructors
  //============================================================================

  _RetainedTabState(this.tab);

  //============================================================================
  // AutomaticKeepAliveClientMixin Methods
  //============================================================================

  @override
  bool get wantKeepAlive => true;

  //============================================================================
  // Lifecycle Methods
  //============================================================================

  @override
  void initState() {
    super.initState();
  }

  @override
  Widget build(BuildContext context) {
    return tab;
  }
}

